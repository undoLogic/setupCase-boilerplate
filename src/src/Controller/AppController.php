class AppController extends Controller
{
    /**
     * Initialization hook method.
     *
     * Use this method to add common initialization code like loading components.
     *
     * e.g. `$this->loadComponent('FormProtection');`
     *
     * @return void
     */
    public function beforeFilter(EventInterface $event)
    {
        //pr($this->request->getAttributes()); exit;



       // exit;
        parent::beforeFilter($event); // TODO: Change the autogenerated stub

        //pr( $this->request->getAttributes()); exit;

        // language
        $current_language = $this->setupLanguage();
        $this->setupAccess();

        $this->viewBuilder()->setLayout('new_layout');
        $this->set('webroot', Router::url('/'));
        $this->set('base', Router::url('/modules'));


        //$this->Authentication->addUnauthenticatedActions();
        $this->Authentication->allowUnauthenticated(['login']);





    }// end of beforeFilter


    public function initialize(): void
    {
        parent::initialize();

        $this->loadComponent('RequestHandler');
        $this->loadComponent('Flash');
        $this->loadComponent('Authentication.Authentication');



    }// end of initialize



    function convertObjToArray($obj) {
        $obj = (array) $obj;
        $obj = json_decode(json_encode($obj), true);

        $obj = array_values($obj);
        foreach($obj as $key => $value){
            if(is_array($value)){
                if(isset($value['user_type_id'])){
                    return $value;
                }
            }
        }

        return false;
    }
    function setupAccess(){

        $access = $this->fetchTable('UserTypes')->find('list', ['fields' => ['id', 'name']])->toArray();

        // /if prefix exists
         if (isset($this->request->getAttributes()['params']['prefix'])) {
            $prefix = $this->request->getAttributes()['params']['prefix'];
            if (in_array($prefix, $access)) {
                // identity
                if (!empty($this->Authentication->getIdentity())) {
                    $identity = $this->Authentication->getIdentity();
                    $userInfo = $this->convertObjToArray($identity);
                    $this->set(compact('userInfo'));

                    if ($access[$userInfo['user_type_id']] !== $prefix) {
                        $this->Flash->error(__('You are not authirized .... '));
                        $this->redirect($this->referer());
                    }else{
                        // proceed to create access to each usertype
                    }
                } else {
                    $this->Flash->error(__('You are not authirized please login first '));
                    $this->redirect('/login');

                }
            }
        }
    }// setupAccess()
    var $language;
    function setupLanguage()
    {
        //$this->session()->write(['current_language' => 'en_US']);

       $url_language = $this->request->getParam('language');
        if ($url_language != null && in_array($url_language, ['en_US', 'fr_CA'])) {
            $current_language = $url_language;
            //add to session
            $this->session()->write(['current_language' => $url_language]);
            $this->language = $current_language;


        }elseif(!empty($this->session()->read('current_language'))) { // session

            $current_language = $this->session()->read('current_language');
            $this->session()->write(['current_language' => $current_language]);
            $this->language = $current_language;
        }elseif(isset($_SERVER['HTTP_ACCEPT_LANGUAGE'])) {
            $current_language = substr($_SERVER['HTTP_ACCEPT_LANGUAGE'], 0, 5);
            $this->session()->write(['current_language' => $current_language]);
            $this->language = $current_language;
        }else{// default manual string
            $current_language = 'en_US';
            $this->language = $current_language;
            $this->session()->write(['current_language' => $current_language]);
            }

        I18n::setLocale($current_language);
        $this->set(compact('current_language'));

        return $current_language;
    }// end of setupLanguage

    private function session(){
        $session = $this->getRequest()->getSession();
        return $session;
    }

    function setCurrentLanguage($lang){
        $this->language = $lang;
        //session
        $this->session()->write(['current_language' => $lang]);
    }

    function getCurrentLanguage(){
        return $this->language;

    }

    function userInfo()
    {
        $userInfo = array();
        if (!empty($this->Authentication->getIdentity())) {
            $identity = $this->Authentication->getIdentity();
            $userInfo = $this->convertObjToArray($identity);

        }
        return $userInfo;

    }

    function getUserId(){
        $user_id = 0;
       $user_info = $this->userInfo();
       if(!empty($user_info)){
           $user_id = $user_info['id'];
       }
        return $user_id;
    }// getUserId


}// END
